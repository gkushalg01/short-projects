"""
A small program to not get banned when you are AFK for 2-3 rounds on valorant 
or any other game that instantly bans you for going AFK. AFKaying is distasteful
but sometimes necessary. So run this program and press 9 to start pressing 
random keys on your keyboard.
Hold 8 to stop the program altogether. 
"""

import pyautogui
import time
import random
import keyboard

def random_key_press():
    keys = ['up', 'down', 'right', 'left']
    key = random.choice(keys)
    pyautogui.keyDown(key)
    time.sleep(0.1)  # Adjust the delay as per your preference
    pyautogui.keyUp(key)

def key_press_listener():
    run = True
    while run:
        if keyboard.is_pressed('9'):
            while True:
                time.sleep(1)
                random_key_press()
                if keyboard.is_pressed('8'):
                    run = False
                    break  # Exit the loop when '8' key is pressed

# Start the key press listener
if __name__=="__main__":
    print("Press '9' to start and Hold '8' to stop.\n")
    key_press_listener()


